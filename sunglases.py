n = int(input())

print('*' * (n * 2) + ' ' * n + '*' * (n * 2))
for _ in range(n - 2):
    if _ == ((n-1) // 2) - 1:
        print('*'  + '/' * (2 * n - 2) + '*' + '|' * (n) + '*'  + '/' * (2 * n - 2) + '*')
    else:
        print('*' + '/' * (2 * n - 2) + '*' + ' ' * (n) + '*' + '/' * (2 * n - 2) + '*')
print('*' * (n * 2) + ' ' * n + '*' * (n * 2))